        with open(os.path.join(content_dir, "image.png"), "wb") as f:
            f.write(b"\x89PNG")
        with open(os.path.join(content_dir, "notes.txt"), "w") as f:
            f.write("plain text")
        rc, _, err = run_ssg(["build"], self.tmpdir)
        self.assertEqual(rc, 0, f"Build failed:\n{err}")
        # Only the .md file produces output
        output = self.tmpdir + "/output"
        self.assertTrue(os.path.isfile(f"{output}/page/index.html"))
        self.assertFalse(os.path.exists(f"{output}/image.png"))
        self.assertFalse(os.path.exists(f"{output}/notes.txt"))

