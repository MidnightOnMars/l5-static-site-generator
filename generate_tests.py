class TestConfig(unittest.TestCase):
    """Tests for configuration loading."""

    def setUp(self):
        self.tmpdir = tempfile.mkdtemp(prefix="ssg_test_")

    def tearDown(self):
        shutil.rmtree(self.tmpdir, ignore_errors=True)

    def test_default_config_when_no_file(self):
        """Config loader returns defaults when site.yaml is missing."""
        create_project(self.tmpdir, config_yaml=None,
                       content_files={"index.md": "# Hello\n"},
                       template_files=MINIMAL_TEMPLATES)
        rc, out, err = run_ssg(["build"], self.tmpdir)
        self.assertEqual(rc, 0, f"Build failed:\n{err}")
        # Default output dir is 'output'
        self.assertTrue(os.path.isdir(os.path.join(self.tmpdir, "output")))

    def test_custom_config_values(self):
        """Config values from site.yaml override defaults."""
        config = "title: Custom Title\noutput_dir: _site\n"
        create_project(self.tmpdir, config_yaml=config,
                       content_files={"index.md": "---\ntitle: Home\n---\n# Home\n"},
                       template_files=MINIMAL_TEMPLATES)
        rc, out, err = run_ssg(["build"], self.tmpdir)
        self.assertEqual(rc, 0, f"Build failed:\n{err}")
        # Custom output dir
        self.assertTrue(os.path.isdir(os.path.join(self.tmpdir, "_site")))
        # Site title in rendered output
        index_html = os.path.join(self.tmpdir, "_site", "index.html")
        self.assertTrue(os.path.isfile(index_html))
        with open(index_html) as f:
            html = f.read()
        self.assertIn("Custom Title", html)

    def test_malformed_yaml_config(self):
        """Malformed YAML in config file produces clear error and exit 1."""
        with open(os.path.join(self.tmpdir, "site.yaml"), "w") as f:
            f.write("title: [unterminated\n")
        os.makedirs(os.path.join(self.tmpdir, "content"), exist_ok=True)
        os.makedirs(os.path.join(self.tmpdir, "templates"), exist_ok=True)
        rc, out, err = run_ssg(["build"], self.tmpdir)
        self.assertNotEqual(rc, 0)
        # Should mention config error, not show Python traceback
        combined = out + err
        self.assertNotIn("Traceback", combined)


