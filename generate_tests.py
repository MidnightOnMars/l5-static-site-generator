def create_project(base, config_yaml=None, content_files=None, template_files=None):
    """Create a minimal SSG project in base directory.

    Args:
        base: Root directory for the project.
        config_yaml: YAML string for site.yaml. None = no config file.
        content_files: Dict of {relative_path: content_string}.
        template_files: Dict of {relative_path: template_string}.
    """
    if config_yaml is not None:
        with open(os.path.join(base, "site.yaml"), "w") as f:
            f.write(config_yaml)

    content_dir = os.path.join(base, "content")
    os.makedirs(content_dir, exist_ok=True)
    if content_files:
        for rel_path, content in content_files.items():
            full = os.path.join(content_dir, rel_path)
            os.makedirs(os.path.dirname(full), exist_ok=True)
            with open(full, "w") as f:
                f.write(textwrap.dedent(content))

    tmpl_dir = os.path.join(base, "templates")
    os.makedirs(tmpl_dir, exist_ok=True)
    if template_files:
        for rel_path, content in template_files.items():
            full = os.path.join(tmpl_dir, rel_path)
            os.makedirs(os.path.dirname(full), exist_ok=True)
            with open(full, "w") as f:
                f.write(textwrap.dedent(content))


BASE_TEMPLATE = """\
<!DOCTYPE html>
<html>
<head><title>{{ page.title }} - {{ site.title }}</title></head>
<body>
<nav>{% for item in nav %}<a href="{{ item.url }}">{{ item.title }}</a> {% endfor %}</nav>
<main>{% block content %}{% endblock %}</main>
</body>
</html>
"""

PAGE_TEMPLATE = """\
{% extends "base.html" %}
{% block content %}
<h1>{{ page.title }}</h1>
{{ page.content }}
{% endblock %}
"""

MINIMAL_TEMPLATES = {
    "base.html": BASE_TEMPLATE,
    "page.html": PAGE_TEMPLATE,
}

MINIMAL_CONFIG = """\
title: Test Site
base_url: /
"""


def run_ssg(args, cwd, env=None):
    """Run the SSG CLI and return (returncode, stdout, stderr)."""
    cmd = [sys.executable, "-m", "ssg"] + args
    run_env = os.environ.copy()
    if env:
        run_env.update(env)
    result = subprocess.run(
        cmd, capture_output=True, text=True, cwd=cwd, env=run_env
    )
    return result.returncode, result.stdout, result.stderr


